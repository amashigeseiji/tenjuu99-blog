import { test, expect } from '@playwright/test'

// このプロジェクト（plans/content-root-switching/）の受け入れ範囲は、ネイティブアプリの
// メニュー操作（コンテンツフォルダを変更…）・AppKitフォルダ選択ダイアログ・サーバープロセスの
// 入れ替えが中心であり、Playwrightが自動操作できるのは「ブラウザでlocalhostのHTTPサーバーへ
// アクセスする」ことだけ。AppKitのメニュー・ダイアログ操作はPlaywrightでは検証できないため、
// plans/content-root-switching/test-tree.md と本ファイルの手動検証記録（実施済み）を正とする。
// 選びなおしロジック自体（切り替え確定／取りやめ継続／blog.json無し拒否）は
// native/Tests/NativeShellCoreTests/ContentRootResolverTests.swift で単体テスト済み（swift run CoreTests）。

test.describe('US-01: 別のプロジェクトへ切り替える', () => {
  test('シナリオ 1: アプリの操作でコンテンツフォルダを変更する', async ({ page }) => {
    // Given: アプリがプロジェクト A をコンテンツルートとして起動している
    // And:   同じ Mac 内に blog.json を持つ別のプロジェクト B がある
    // When:  利用者がアプリの通常の操作の範囲でコンテンツフォルダの変更を選び、プロジェクト B を指定する
    // Then:  アプリはプロジェクト B の内容で使える状態になる
    // And:   永続化ストアの手動操作など、アプリ外部の知識を必要としない
    test.skip(
      true,
      '実機で手動検証済み（2026-07-08）。プロジェクトA（dev/nobi）で起動中の.appの' +
        'メニューバー「ファイル > コンテンツフォルダを変更…」からフォルダ選択ダイアログを開き、' +
        'blog.jsonを持つ別プロジェクトBを選択 → サーバープロセスがBを作業場所として入れ替わり' +
        '（lsofでcwd確認）、永続化ストアの記憶もBに書き換わり、editor.htmlがHTTP 200を返し、' +
        'B直下にnode_modulesのモジュール解決リンクも自動作成されることを確認。' +
        'UserDefaultsの手動操作は一切不要。'
    )
  })

  test('シナリオ 2: 切り替え後の記憶が引き継がれる', async ({ page }) => {
    // Given: 利用者がプロジェクト A からプロジェクト B へ切り替えた
    // When:  アプリを終了して再起動する
    // Then:  ダイアログなしでプロジェクト B のまま立ち上がる
    test.skip(
      true,
      '実機で手動検証済み（2026-07-08）。切り替え後にメニューからアプリを終了' +
        '（サーバープロセスも残らず停止することをpgrepで確認）→ `open`で再起動 → ' +
        'フォルダ選択ダイアログは表示されず（ウィンドウはメインのみ）、サーバーはプロジェクトBを' +
        '作業場所として起動し、editor.htmlがHTTP 200を返すことを確認。'
    )
  })

  test('シナリオ 3: 切り替え操作を途中でやめる', async ({ page }) => {
    // Given: アプリがプロジェクト A をコンテンツルートとして起動している
    // When:  利用者がコンテンツフォルダの変更操作を始めたが、フォルダを選択せずにやめる
    // Then:  アプリはプロジェクト A のまま使い続けられる
    test.skip(
      true,
      '実機で手動検証済み（2026-07-08）。「コンテンツフォルダを変更…」でダイアログを開いた後、' +
        'キャンセルボタンで閉じる → サーバープロセスは入れ替わらず（PID不変）、' +
        '記憶もプロジェクトAのまま、editor.htmlはHTTP 200を返し続けることを確認。' +
        'アプリが終了してしまう初回解決のエラー動作（userCancelled→terminate）が混入していないこと、' +
        'すなわちキャンセルが「現状維持」として扱われることを単体テスト' +
        '（testContentRootReselect）でも保証している。'
    )
  })
})
